"""
Utility functions
"""

def sum(num1=0, num2=0):
    """
    Sum two numbers
    """
    return num1 + num2

def sub(num1, num2):
    """
    Subtract two numbers
    """
    return num1 - num2

def mul(num1, num2):
    """
    Multiply two numbers
    """
    return num1 * num2

def div(num1, num2):
    """
    Divide two numbers
    """
    return num1 / num2
